# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def mergeTwoLists(self, list1: Optional[ListNode], list2: Optional[ListNode]) -> Optional[ListNode]:
        
        ## 2 pointers and no copying of l1 or l2 to dummy node 
        
        
        ## tc : l1 = [1,2] , l2 =[3,4]
        
        l1=list1
        l2= list2
        
        dummy = ListNode(0)
        curr = dummy
        while l1 and l2 :
            
            if l1.val < l2.val:
                curr.next = l1
                l1 = l1.next
            else:
                curr.next = l2
                l2 = l2.next
            curr = curr.next
        curr.next = l1 if l1 else l2
        
        return dummy.next
